#define LOW_LIMIT -2147483648

void	ft_putchar(char c);

void	ft_putnbr(int nb)
{
	if (nb < 0)
	{
		nb = -nb;
		ft_putchar('-');
	}
	if (nb <= 2147483647 && nb >= -2147483647)
	{
		if (nb > 9 || nb < -9)
			ft_putnbr(nb / 10);
		ft_putchar('0' + nb % 10);
	}
}